//Aula: Funções
//nome da função (parametros que serão usados na função)
function soma (numero_1, numero_2) {
    //corpo da função e o que aconterá quando ela for chamada
    var resultado = numero_1+numero_2;
    return resultado;
}
//passar os valores do parametro
var resultado = soma(1,2);
console.log(resultado);
